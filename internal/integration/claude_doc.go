// Package integration owns the authoritative agent-facing documentation that
// autoresearch writes into a target project's .claude/ directory. Keeping the
// content in a Go package (not a loose template file) lets `autoresearch init`
// and `autoresearch claude install` share the same source of truth and
// version-bump it atomically.
package integration

import "strings"

// ClaudeDocRelPath is the path to write under the target project, relative to
// the project root.
