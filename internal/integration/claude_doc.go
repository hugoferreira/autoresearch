// ClaudeDoc returns the full content of .claude/autoresearch.md. The opening
// warning and the managed sentinels make it obvious to a future editor that
// autoresearch owns this file.
func ClaudeDoc(moduleVersion string) string {
	return strings.ReplaceAll(claudeDocTemplate, "{{version}}", moduleVersion)
}

